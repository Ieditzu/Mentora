export function translate(language, text, variables = {}) {
  const catalogs = { ro: romanian, fr: french, de: german };
  const template = catalogs[language]?.[text] || text;
  return template.replace(/{{(\w+)}}/g, (_, key) => String(variables[key] ?? ""));
}
